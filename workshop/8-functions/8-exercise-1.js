// Q1
// Write a function that accepts two integers and displays
// the greater one, using `console.log`

// Define the function
function max(num1, num2) {

    if (num1 > num2){
        return (num1)
    }   else {
        return (num2)
    }
}

console.log('A8.1 ', (max(12,43)));
// Call the function
max(12, 43);
